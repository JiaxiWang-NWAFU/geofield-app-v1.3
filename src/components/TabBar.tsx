const TabBar: React.FC = () => {
  const navigate = useNavigate();
  const location = useLocation();

  const isActive = (path: string) => {
    if (path === '/quadrat') return location.pathname.startsWith('/quadrat');
    if (path === '/investigation') return location.pathname.startsWith('/investigation');
    return location.pathname === path;
  };

  return (
    <nav className="flex-shrink-0 bg-white border-t border-gray-200 shadow-sm z-40 safe-area-bottom">
      <div className="flex items-center justify-around h-14">
        {tabs.map((tab) => {
          const active = isActive(tab.path);
          return (
            <button
              key={tab.path}
              onClick={() => navigate(tab.path)}
              className={`flex flex-col items-center justify-center gap-0.5 flex-1 h-full transition-colors duration-150 relative ${
                active ? 'text-primary-600' : 'text-gray-400 active:text-gray-600'
              }`}
            >
              <div className={`transition-transform ${active ? 'scale-110' : ''}`}>
                {tab.icon}
              </div>
              <span className={`text-[10px] leading-tight ${active ? 'font-semibold' : ''}`}>
                {tab.label}
              </span>
              {active && (
                <div className="absolute top-0 left-1/2 -translate-x-1/2 w-8 h-0.5 bg-primary-500 rounded-b" />
              )}
            </button>
          );
        })}
      </div>
    </nav>
  );
};

export default TabBar;
